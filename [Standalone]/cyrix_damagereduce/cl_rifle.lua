
Citizen.CreateThread(function()
    while true do
        SetWeaponDamageModifier(`weapon_assaultrifle`, 1.0) -- Weapon Label (Assault Rifle)
        SetWeaponDamageModifier(`weapon_carbinerifle`, 1.0) -- Weapon Label (Carbine Rifle)
        SetWeaponDamageModifier(`weapon_advancedrifle`, 1.0) -- Weapon Label (Advanced Rifle)
        SetWeaponDamageModifier(`weapon_specialcarbine`, 1.0) -- Weapon Label (Special Carbine)
        SetWeaponDamageModifier(`weapon_bullpuprifle`, 1.0) -- Weapon Label (Bullpup Rifle)
        SetWeaponDamageModifier(`weapon_compactrifle`, 1.0) -- Weapon Label (Compact Rifle)
        SetWeaponDamageModifier(`weapon_militaryrifle`, 1.0) -- Weapon Label (Military Rifle)
-- MK2 Rifles
        SetWeaponDamageModifier(`weapon_assaultrifle_mk2`, 1.0) -- Weapon Label (Assault Rifle Mk II)
        SetWeaponDamageModifier(`weapon_carbinerifle_mk2`, 1.0) -- Weapon Label (Carbine Rifle Mk II)
        SetWeaponDamageModifier(`weapon_specialcarbine_mk2`, 1.0) -- Weapon Label (Special Carbine Mk II)
        SetWeaponDamageModifier(`weapon_bullpuprifle_mk2`, 1.0) -- Weapon Label (Bullpup Rifle Mk II)
	Wait(0)
    end
end)
